     * @return \Illuminate\Http\Response
     */
    public function getTodo(Request $request)
    {
        $user_id = $request['id_user'];
        $cats = Category::all() ;
        $retour = [];
        foreach($cats as $cat){
            if( count($cat->todos()->where('id_user' , $user_id)->get()) != 0 ){
                $retour[] = [$cat['name'] => $cat->todos()->where('id_user' , $user_id)->get()];
            }
        }
        return $retour;
    }

